# 5-7. Favorite Fruit:
# Make a list of your favorite fruits, and then write a series of
# independent if statements that check for certain fruits in your list.

# 1 Make a list of your three favorite fruits and call it favorite_fruits.
# 2 Write five if statements. Each should check whether a certain kind of fruit
#   is in your list. If the fruit is in your list, the if block should print a statement,
#   such as You really like bananas!

favorite_fruits = ["banana", "pear", "apricot"]

if "banana" in favorite_fruits:
    print("I see you like bananas")
if "apple" in favorite_fruits:
    print("I see you like apples")
else:
    print("Not keen on apples?")
if "pear" in favorite_fruits:
    print("I see you like pears")
if "peach" in favorite_fruits:
    print("I see you like peaches")
if "apricot" in favorite_fruits:
    print("I see you like apricots")
