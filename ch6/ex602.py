# 6-2. Favorite Numbers:
# Use a dictionary to store people’s favorite numbers.
# Think of five names, and use them as keys in your dictionary.
# Think of a favorite number for each person, and store each as a value in your dictionary.
# Print each person’s name and their favorite number.

favourite_numbers = {
    "mick": 13,
    "viv": 5,
    "jessica": 32,
    "nugget": 7,
    "jasmine": 12,
}

print(f"Mick's favourite number is {favourite_numbers['mick']}")
print(f"Viv's favourite number is {favourite_numbers['viv']}")
print(f"Jessica's favourite number is {favourite_numbers['jessica']}")
print(f"Nugget's favourite number is {favourite_numbers['nugget']}")
print(f"Jasmine's favourite number is {favourite_numbers['jasmine']}")
